namespace GAME
{
	USIZE Dx_UTL::TxSize ( TX tx )
	{
		D3DSURFACE_DESC dc;
		tx->GetLevelDesc ( 0, & dc );
		USIZE usize = { dc.Width, dc.Height };
		return usize;
	}

}	//namespace GAME

